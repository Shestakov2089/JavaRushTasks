package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int count1 = 0;

        while(count1 < 5){
            int count2 = 0;
            while (count2 < 10){
                System.out.print("Q");
                count2++;
            }
            System.out.println("");
            count1++;
        }

    }
}